<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												shipinmingcheng: '',
																shipinleixing: '',
																fengmiantu: '',
																shipin: '',
																shipinjieshao: '',
																faburiqi: '',
																												userid: '',
												},
																				shipinleixingOptions: [],
				shipinleixingIndex: 0,
																																																												// 登陆用户信息
				user: {},
                                ro:{
                                   shipinmingcheng : false,
                                   shipinleixing : false,
                                   fengmiantu : false,
                                   shipin : false,
                                   shipinjieshao : false,
                                   faburiqi : false,
                                   clicknum : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																						
																		// 下拉框
			res = await this.$api.option(`shipinleixing`,`leixing`,{});
			this.shipinleixingOptions = res.data;
																																													
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jianshenshipin`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='shipinmingcheng'){
					this.ruleForm.shipinmingcheng = obj[o];
					this.ro.shipinmingcheng = true;
					continue;
					}
					if(o=='shipinleixing'){
					this.ruleForm.shipinleixing = obj[o];
					this.ro.shipinleixing = true;
					continue;
					}
					if(o=='fengmiantu'){
					this.ruleForm.fengmiantu = obj[o];
					this.ro.fengmiantu = true;
					continue;
					}
					if(o=='shipin'){
					this.ruleForm.shipin = obj[o];
					this.ro.shipin = true;
					continue;
					}
					if(o=='shipinjieshao'){
					this.ruleForm.shipinjieshao = obj[o];
					this.ro.shipinjieshao = true;
					continue;
					}
					if(o=='faburiqi'){
					this.ruleForm.faburiqi = obj[o];
					this.ro.faburiqi = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																						
			// 多级联动参数
																																																						
																																							faburiqiChange(e) {
				this.ruleForm.faburiqi = e.target.value;
				this.$forceUpdate();
			},
																		
																																																			
															// 下拉变化
			shipinleixingChange(e) {
				this.shipinleixingIndex = e.target.value
				this.ruleForm.shipinleixing = this.shipinleixingOptions[this.shipinleixingIndex]
			},
																																										
																											fengmiantuTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fengmiantu = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
															shipinTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.shipin = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																													
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																												if((!this.ruleForm.shipinmingcheng)){
					this.$utils.msg(`视频名称不能为空`);
					return
				}
																																																																																																																				if((!this.ruleForm.shipin)){
					this.$utils.msg(`视频不能为空`);
					return
				}
																																																																																																																								if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																																																								if(this.ruleForm.id){
					await this.$api.update(`jianshenshipin`, this.ruleForm);
				}else{
					await this.$api.add(`jianshenshipin`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

